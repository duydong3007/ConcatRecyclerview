package com.example.concatrecyclerview;

public class category {
    private int anhcate;
    private String name;

    public category(int anhcate, String name) {
        this.anhcate = anhcate;
        this.name = name;
    }

    public int getAnhcate() {
        return anhcate;
    }

    public void setAnhcate(int anhcate) {
        this.anhcate = anhcate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
